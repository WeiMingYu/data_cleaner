# master_schema.py
# 主檔固定欄位定義 — 唯一真相，所有層都參考此處
# 任何欄位異動只改這裡，不散落各模組
#
# 主檔輸出：Excel (.xlsx)
# 欄位順序即 Excel 輸出的欄位順序

# ===== 1. 主檔標準欄位（順序固定）=====
MASTER_COLUMNS = [
    "公私立",       # 公立 / 私立 / 空白
    "分類",         # 幼兒園 / 國小 / 國中 / 高中 ...
    "縣市區",       # 臺北市 / 新北市 ...
    "鄉鎮市區",     # 中正區 / 板橋區 ... （無則空白）
    "名稱",         # 機構名稱（必填）
    "地址",         # 完整地址
    "Mail 電子郵件",# email
    "電話",         # 聯絡電話
    "傳真",         # 傳真號碼
    "網址",         # 官方網站 URL
    "備註",         # 備用欄位
    "資料來源",     # 資料來源說明（例：全國教保資訊網、政府開放資料）
]

# ===== 2. 必填欄位（清洗後不可為空）=====
REQUIRED_COLUMNS = [
    "名稱",         # 沒有名稱的資料視為無效
]

# ===== 3. 用來判斷是否為重複資料的候選欄位 =====
# 實際比對鍵由 config.py 的 MATCH_KEYS 定義
# 這裡只是列出「有意義的」候選欄位，供使用者參考
DEDUP_CANDIDATE_COLUMNS = [
    "名稱",
    "電話",
    "地址",
    "縣市區",
]

# ===== 4. 產生一個空的標準格式 dict（供清洗層填充）=====
def empty_record() -> dict:
    """回傳一筆空的主檔格式 dict，所有欄位預設為空字串"""
    return {col: "" for col in MASTER_COLUMNS}
